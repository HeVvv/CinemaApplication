    private String name;
    private int typeId;
    private String typeName;
    private int theaterId;
    private String theaterName;
    private int saleszoneId;
    private String saleszoneName;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

